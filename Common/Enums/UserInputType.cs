namespace Common.Enums
{
	/// <summary>
	/// Input types that are applicable for user inputs
	/// </summary>
	public enum UserInputType
	{
		/// <summary>
		/// Basic text field input
		/// </summary>
		Text,

		/// <summary>
		/// Combobox selection style input
		/// </summary>
		Selection,

		/// <summary>
		/// Combobox selection style input where selection options are generated via a script
		/// </summary>
		ScriptedSelection
	}
}
